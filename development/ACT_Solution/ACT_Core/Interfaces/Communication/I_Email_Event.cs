using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ACT.Core.Interfaces.Communication
{
    ///-------------------------------------------------------------------------------------------------
    /// <summary>   Interface for email event. </summary>
    ///
    /// <remarks>   Mark Alicz, 9/23/2019. </remarks>
    ///-------------------------------------------------------------------------------------------------

    public interface I_Email_Event
    {
        ///-------------------------------------------------------------------------------------------------
        /// <summary>   Gets or sets the date. </summary>
        ///
        /// <value> The date. </value>
        ///-------------------------------------------------------------------------------------------------

        string Date { get; set; }

        ///-------------------------------------------------------------------------------------------------
        /// <summary>   Gets or sets the event. </summary>
        ///
        /// <value> The event. </value>
        ///-------------------------------------------------------------------------------------------------

        string EventEvent { get; set; }

        ///-------------------------------------------------------------------------------------------------
        /// <summary>   Gets or sets the username. </summary>
        ///
        /// <value> The username. </value>
        ///-------------------------------------------------------------------------------------------------

        string Username { get; set; }

        ///-------------------------------------------------------------------------------------------------
        /// <summary>   Gets or sets the source for the. </summary>
        ///
        /// <value> from. </value>
        ///-------------------------------------------------------------------------------------------------

        string From { get; set; }

        ///-------------------------------------------------------------------------------------------------
        /// <summary>   Gets or sets the recipient. </summary>
        ///
        /// <value> The recipient. </value>
        ///-------------------------------------------------------------------------------------------------

        string Recipient { get; set; }

        ///-------------------------------------------------------------------------------------------------
        /// <summary>   Gets or sets the subject. </summary>
        ///
        /// <value> The subject. </value>
        ///-------------------------------------------------------------------------------------------------

        string Subject { get; set; }

        ///-------------------------------------------------------------------------------------------------
        /// <summary>   Gets or sets the reason. </summary>
        ///
        /// <value> The reason. </value>
        ///-------------------------------------------------------------------------------------------------
        string Reason { get; set; }


        ///-------------------------------------------------------------------------------------------------
        /// <summary>   Gets or sets the emailid. </summary>
        ///
        /// <value> The emailid. </value>
        ///-------------------------------------------------------------------------------------------------
        string EmailID { get; set; }

        ///-------------------------------------------------------------------------------------------------
        /// <summary>   Initializes this object from the given JSON. </summary>
        ///
        /// <param name="JSON"> The JSON. </param>
        ///-------------------------------------------------------------------------------------------------
        void FromJson(string JSON);

        ///-------------------------------------------------------------------------------------------------
        /// <summary>   Converts this object to a JSON. </summary>
        ///
        /// <returns>   This object as a string. </returns>
        ///-------------------------------------------------------------------------------------------------

        string ToJson();
    }
}
